#!/bin/python3
#range(10) means==> (start_value = 0; start_value < 10; value +=1)
#It will print 0,1,2,.....7,8,9
for num in range(10):
    print(num)

print("\n")
#range(1, 10) means==> (start_valu = 1; star_value < 10; value+=1).
#It will print 1,2,3.....7,8,9
for num in range(1, 10):
    print(num)

print("\n")
#range(1, 10, 2) means==> (start_value = 1; start_value < 10; value+=2).
#It will print 1,3,5,7,9
for num in range(1, 10, 2):
    print(num)